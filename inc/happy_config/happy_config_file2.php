(1) 쿠키 설정을 거부하는 방법으로는 회원이 사용하는 웹 브라우저의 옵션을 선택함으로써 모든 쿠키를 허용하거나 쿠키를 저장할 때마다 확인을 거치거나, 모든 쿠키의 저장을 거부할 수 있습니다.<br />
(2) 웹 브라우저 설정을 통해 쿠키 설정을 변경할 수 있습니다. 단, 회원이 쿠키 설치를 거부하였을 경우 일부 서비스 제공에 어려움이 있을 수 있습니다.<br />
- 크롬의 경우 브라우저 상단의 “설정&gt;개인정보 및 보안”을 통해 설정 가능<br />
